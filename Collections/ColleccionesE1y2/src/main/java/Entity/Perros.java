public class Perros {
    
    private String raza;
   
    public Perros(){
        
    }
    
    public Perros(String raza){
        this.raza = raza;
    }

    public String getRaza() {
        return raza;
    }

    public void setRaza(String raza) {
        this.raza = raza;
    }

    @Override
    public String toString() {
        return "Perros{" + "raza=" + raza + '}';
    }
    
    
}
